#ifndef LEDS_H_
#define LEDS_H_

// define pin numbers

#define PF1    1U
#define PF2    2U
#define PF3    3U
#define PA0    0U
#define PA1    1U
#define PA2    2U
#define PA3    3U
#define PA4    4U

// define led pin configurations
#define DRIVER_RED_LED       PF1      // RED led of the driver is connected to pf1
#define DRIVER_BLUE_LED      PF2      // blue led of the driver is connected to pf2
#define DRIVER_GREEN_LED     PF3      // green led of the driver is connected to pf3
#define PASSENGER_RED_LED    PA2      // RED led of the passenger is connected to PA0
#define PASSENGER_BLUE_LED   PA3      // blue led of the passenger is connected to PA1
#define PASSENGER_GREEN_LED  PA4      // green led of the passenger is connected to PA2
// define



// define
#endif /* LEDS_H_ */
